package packOhioCovidCounties;

public class County {

	String name = "";
	int cases = 0;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	
	public int getCases() {
		return cases;
	}
	
	public void setCases(int cases) {
		this.cases = cases;
	}
	
}
